package atividadesPOO2;

public class Carro {
	String cor;
	int portas;
	String tipo;
	String marca;
	int rodas;
	String modelo;
	int aceleracao;
	int velocMed;
	int velocMax;
	String usarFarol;
	
	public String getCor() {
		return cor;
	}
	public void setCor (String cor) {
		this.cor = cor;
	}
	public int getPortas() {
		return portas;
	}
	public void setPortas (int portas) {
		this.portas = portas;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo (String tipo) {
		this.tipo = tipo;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca (String marca) {
		this.marca = marca;
	}
	public int getRodas() {
		return rodas;
	}
	public void setRodas (int rodas) {
		this.rodas = rodas;	
	}
	public String getModelo() {
		return modelo;
	}
	public void setModelo (String modelo) {
		this.modelo = modelo;
	}
	public int getAceleracao() {
		return aceleracao;
	}
	public void setAceleracao (int aceleracao) {
		this.aceleracao = aceleracao;
	}
	public int getVelocMed() {
		return velocMed;
	}
	public void setVelocMed (int velocMed) {
		this.velocMed = velocMed;
	}
	public int getVelocMax() {
		return velocMax;
	}
	public void setVelocMax (int velocMax) {
		this.velocMax = velocMax;
	}
	public String getUsarFarol() {
		return usarFarol;
	}
	public void setUsarFarol (String usarFarol) {
		this.usarFarol = usarFarol;
	}
}