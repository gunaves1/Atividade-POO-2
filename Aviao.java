package atividadesPOO2;

public class Aviao {
	String cor;
	String companhia;
	int janelas;
	int rodas;
	String abrePorta;
	String girarTurbinas;
	String subir;
	String descer;
	
	public String getCor() {
		return cor;
	}
	public void setCor (String cor) {
		this.cor = cor;
	}
	public String getCompanhia() {
		return companhia;
	}
	public void setCompanhia(String companhia) {
		this.companhia = companhia;
	}
	public int getJanelas() {
		return janelas;
	}
	public void setJanelas (int janelas) {
		this.janelas = janelas;
	}
	public int getRodas() {
		return rodas;
	}
	public void setRodas (int rodas) {
		this.rodas = rodas;
	}
	public String getAbrePorta() {
		return abrePorta;
	}
	public void setAbrePorta (String abrePorta) {
		this.abrePorta = abrePorta;
	}
	public String getGirarTurb() {
		return girarTurbinas;
	}
	public void setGirarTurb (String girarTurbinas) {
		this.girarTurbinas = girarTurbinas;
	}
	public String getSubir() {
		return subir;
	}
	public void setSubir (String subir) {
		this.subir = subir;
	}
	public String getDescer() {
		return descer;
	}
	public void setDescer (String descer) {
		this.descer = descer;
	}
}
