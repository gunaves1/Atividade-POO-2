package atividadesPOO2;

public class Cliente {
	int id;
	String nome;
	String telefone;
	String cpf;
	String rg;
	String anda;
	String trabalha;
	String estuda;
	String canta;
	
	public String getNome() {
		return nome;
	}
	public void setNome (String nome) {
		this.nome = nome;
	}
	public int getId() {
		return id;
	}
	public void setId (int id) {
		this.id = id;
	}
	public String getTelefone() {
		return telefone;
	}
	public void setTelefone (String telefone) {
		this.telefone = telefone;
	}
	public String getCpf() {
		return cpf;
	}
	public void setCpf (String cpf) {
		this.cpf = cpf;
	}
	public String getRg() {
		return rg;
	}
	public void setRg (String rg) {
		this.rg = rg;
	}
	public String getAnda() {
		return anda;
	}
	public void setAnda (String anda) {
		this.anda = anda;
	}
	public String getTrabalha() {
		return trabalha;
	}
	public void setTrabalha (String trabalha) {
		this.trabalha = trabalha;
	}
	public String getEstuda() {
		return estuda;
	}
	public void setEstuda (String estuda) {
		this.estuda = estuda;
	}
	public String getCanta() {
		return canta;
	}
	public void setCanta (String canta) {
		this.canta = canta;
	}
}